// Current language
let currentLang = localStorage.getItem('dargol_lang') || 'en';

/**
 * Apply translations to the page
 */
function applyTranslations(lang) {
  const dict = TRANSLATIONS[lang];
  if (!dict) return;

  // Update all [data-i18n] elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (dict[key]) el.innerHTML = dict[key];
  });

  // Update placeholder attributes
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (dict[key]) el.placeholder = dict[key];
  });

  // Update HTML dir and lang
  const isRTL = lang === 'ar' || lang === 'ku';
  document.documentElement.setAttribute('lang', lang);
  document.documentElement.setAttribute('dir', isRTL ? 'rtl' : 'ltr');
  document.body.setAttribute('dir', isRTL ? 'rtl' : 'ltr');

  // Update active lang button (side menu)
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
  });

  // Update navbar dropdown active item + label
  const langLabels = { en: 'English', ku: 'کوردی', ar: 'العربية' };
  const labelEl = document.getElementById('langDropdownLabel');
  if (labelEl) labelEl.textContent = langLabels[lang] || 'English';

  document.querySelectorAll('.lang-dropdown__item').forEach(item => {
    item.classList.toggle('active', item.getAttribute('data-lang') === lang);
  });

  currentLang = lang;
  localStorage.setItem('dargol_lang', lang);
}

/**
 * Switch language
 */
function switchLang(lang) {
  applyTranslations(lang);
  showToast('Language changed successfully! 🌐');

  // Seamless UX: automatically close the side menu if it's open
  if (typeof closeSideMenu === 'function') {
    closeSideMenu();
  }
}

/**
 * Navbar Language Dropdown
 */
function toggleLangDropdown() {
  const dropdown = document.getElementById('langDropdown');
  if (dropdown) dropdown.classList.toggle('open');
}

function closeLangDropdown() {
  const dropdown = document.getElementById('langDropdown');
  if (dropdown) dropdown.classList.remove('open');
}

// Close dropdown when clicking outside
document.addEventListener('click', function(e) {
  const dropdown = document.getElementById('langDropdown');
  if (dropdown && !dropdown.contains(e.target)) {
    dropdown.classList.remove('open');
  }
});

/**
 * Initialize i18n on page load
 */
document.addEventListener('DOMContentLoaded', () => {
  applyTranslations(currentLang);
});
